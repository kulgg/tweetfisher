import ProgressBar from "@ramonak/react-progress-bar";
import { useQuery } from "@tanstack/react-query";
import { setMaxIdleHTTPParsers } from "http";
import { type NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import React, { useEffect, useState } from "react";
import LoadingAnimation from "../components/loading-animation";
import LoadingMessage from "../components/loading-message";
import Tweet from "../components/tweet";
import isDeleted from "../utils/twitter";

function delay(ms: number) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

const Home: NextPage = () => {
  const [step, setStep] = useState<1 | 2 | 3 | 4>(1);
  const [usernameInput, setUsernameInput] = useState("");
  const [currentUsername, setCurrentUsername] = useState("");
  const [deletedTweets, setDeletedTweets] = useState<string[]>([]);
  const [numFetched, setNumFetched] = useState(0);

  const archiveQuery = useQuery({
    queryKey: ["webarchive"],
    queryFn: async () => {
      const response = await fetch(`/api/archive/${usernameInput}`);
      const result = await response.json();
      return result;
    },
    enabled: false,
  });

  const isStepTwo = useEffect(() => {
    if (
      !archiveQuery.data ||
      archiveQuery.data.statusUrls === undefined ||
      step > 2
    ) {
      return;
    }

    setStep(2);
  }, [archiveQuery.data]);

  console.log(usernameInput);
  console.log("data", archiveQuery.data);
  console.log("deleted", deletedTweets);

  const handleSubmit = (e: React.FormEvent<HTMLFormElement>) => {
    setStep(1);
    e.preventDefault();
    console.log("submitted");
    setCurrentUsername(usernameInput);
    archiveQuery.refetch();
    setUsernameInput("");
  };

  console.log("isStepTwo", isStepTwo);

  useEffect(() => {
    switch (step) {
      case 2:
        setStep(3);
        for (let i = 0; i < archiveQuery.data.statusUrls.length; i++) {
          delay(1000).then(() => {
            isDeleted(archiveQuery.data.statusUrls[i]).then((x) => {
              if (x) {
                setDeletedTweets((prev) => [
                  ...prev,
                  archiveQuery.data.statusUrls[i],
                ]);
              }
              setNumFetched((prev) => prev + 1);
              if (i === archiveQuery.data.statusUrls.length - 1) {
                setStep(4);
              }
            });
          });
        }
        break;
    }
  }, [step]);

  return (
    <>
      <Head>
        <title>DeletedTweets</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="min-h-screen overflow-hidden ">
        <div className="container mx-auto flex flex-col items-center">
          <h1 className="mr-auto mt-2 bg-gradient-to-r from-emerald-200 to-rose-200 bg-clip-text text-[39px] font-bold text-transparent sm:mt-20 sm:text-7xl">
            Find Deleted Tweets
          </h1>
          <div className="mt-10 w-full sm:mt-20">
            <form onSubmit={handleSubmit} className="relative">
              <label
                htmlFor="search"
                className="block text-lg font-medium text-indigo-200"
              >
                Enter a username
              </label>
              <input
                type="text"
                name="search"
                autoComplete="off"
                id="search"
                placeholder="elonmusk"
                className="mt-1 block h-12 w-full rounded-md border-gray-500 bg-neutral-900 text-lg text-gray-100 shadow-sm placeholder:text-gray-500 focus:border-[1px] focus:border-gray-300 focus:ring-1 focus:ring-emerald-700"
                value={usernameInput}
                onChange={(e) => setUsernameInput(e.currentTarget.value)}
              />
            </form>
          </div>
          <div className="mt-6"></div>
          {currentUsername && (
            <div className="mb-3 mr-auto rounded-lg bg-gray-800 py-1 px-2 text-lg text-zinc-300">
              @{currentUsername}
            </div>
          )}
          {archiveQuery.isFetching && (
            <LoadingMessage message="Searching for archived tweets" />
          )}
          {step >= 2 && (
            <div className="">
              <div className="grid grid-cols-3 gap-2 text-lg">
                <span className="text-right font-semibold text-emerald-200">
                  {archiveQuery.data.statusUrls.length}
                </span>
                <div className="col-span-2">archived tweets.</div>
              </div>
            </div>
          )}
          {step === 3 && (
            <div className="my-7">
              <LoadingMessage message="Checking for deleted tweets" />
              <div className="my-2"></div>
              <ProgressBar
                completed={numFetched}
                maxCompleted={archiveQuery.data.statusUrls.length}
                className="w-full rounded-full border border-gray-400"
                bgColor="#a7f3d0"
                baseBgColor="#374151"
                isLabelVisible={false}
              />
            </div>
          )}
          {step >= 4 && (
            <div className="">
              <div className="grid grid-cols-3 gap-2 text-lg">
                <span className="text-right font-semibold text-rose-200">
                  {deletedTweets.length}
                </span>
                <div className="col-span-2">deleted tweets.</div>
              </div>
            </div>
          )}
          {/* <Tweet
            pfp={
              "https://pbs.twimg.com/profile_images/1590968738358079488/IY9Gx6Ok_400x400.jpg"
            }
            text={
              "Next Twitter update will remember whether you were on For You (ie recommended), Following or list you made & stop switching you back to recommended tweets"
            }
            url={"https://twitter.com/disclosetv/status/1617540634482708481"}
            username="Elon Musk"
            handle={"elonmusk"}
            created={new Date()}
          /> */}
        </div>
      </main>
    </>
  );
};

export default Home;
